    /**
     * Gets the current location of the device, and positions the map's camera.
     */
    private void getDeviceLocation(final ShowCurrentLocation fromHotel) {
        //Try Function
        try {
            //Check Izin GPS
            if (mLocationPermissionGranted) {
                //Cek GPS Aktif
                if(locationManager.isProviderEnabled(LocationManager.GPS_PROVIDER))
                {
                    //Cek Koneksi Internet
                    if (checkInternet(Peta.this)){
                        //Dapatkan Lokasi Sekarang
                        Task<Location> locationResult = mFusedLocationProviderClient.getLastLocation();
                        //Mendapatkan Lokasi Sekarang
                        locationResult.addOnCompleteListener(this, new OnCompleteListener<Location>() {
                            @Override
                            public void onComplete(@NonNull Task<Location> task) {
                                //Jika Berhasil Mendapatkan Lokasi
                                if (task.isSuccessful()) {
                                    // Set the map's camera position to the current location of the device.
                                    mLastKnownLocation = task.getResult();
//                            Log.i(TAG, "onComplete: " + task.getResult());
                                    CurrentPosition = new LatLng(mLastKnownLocation.getLatitude(),
                                            mLastKnownLocation.getLongitude());
                                } else {
                                    //Gagal Mendapatkan Lokasi
                                    CurrentPosition = mDefaultLocation;
                                    mMap.getUiSettings().setMyLocationButtonEnabled(false);
                                }
                                LatLng kios_lat = new LatLng(kios_models[position_kios].getLatitude(), kios_models[position_kios].getLongitude());
                                String distance = String.valueOf((int)Particle.Distance(CurrentPosition,kios_lat));
                                Snackbar snackbar = Snackbar
                                        .make(coordinatorLayout, "Jarak : "+distance +" meter", Snackbar.LENGTH_INDEFINITE);

                                snackbar.show();
                                //Periksa Masuk dari button rute
                                if (fromHotel == ShowCurrentLocation.No) {
                                    LatLng temp = new LatLng(kios_models[position_kios].getLatitude(), kios_models[position_kios].getLongitude());
                                    mMap.moveCamera(CameraUpdateFactory.newLatLngZoom(temp, DEFAULT_ZOOM));
                                }else{
                                    //Dari menu maps
                                    mMap.moveCamera(CameraUpdateFactory.newLatLngZoom(CurrentPosition, DEFAULT_ZOOM));
                                }
                                //Set Marker

                                SetMarker(CurrentPosition,"Posisi Sekarang",0.0F);
                            }
                        });
                        //Tidak Terhubung Internet
                    }else{
                        Toast.makeText(Peta.this, "Tidak Terhubung Dengan Internet", Toast.LENGTH_SHORT).show();
                    }
                    //GPS Tidak Aktif
                }else {
                    Toast.makeText(Peta.this, "Aktifkan GPS", Toast.LENGTH_SHORT).show();
                }
                //GPS Tidak Diizinkan
            }else {
                Toast.makeText(Peta.this, "Izinkan GPS", Toast.LENGTH_SHORT).show();
            }
            //Catch Error
        } catch (SecurityException e)  {
            Log.e("Exception: %s", e.getMessage());
        }
    }

